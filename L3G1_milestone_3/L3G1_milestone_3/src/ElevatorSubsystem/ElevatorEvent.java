package ElevatorSubsystem;

/**
 * Enumerates events that can occur in the elevator system state machine.
 * Includes events such as going up, going down, opening door, closing door, and stopping.
 */
public enum ElevatorEvent {
    GOING_UP,
    GOING_DOWN,
    OPEN_DOOR,
    CLOSE_DOOR,
    STOP
}

